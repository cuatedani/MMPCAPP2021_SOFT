    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int id = (int) Usuario[0];
        if (id != BD.GetUsuarioSesion()) {
            BD.EliminarUsuario(id);
            this.setVisible(false);
            InterfaceGestionUsuario GestionUsuario = new InterfaceGestionUsuario(BD);
            GestionUsuario.setVisible(true);
            this.dispose();
            return;
        }
        JOptionPane.showMessageDialog(null, "No Puedes Eliminar un Usuario con Sesion Activa");
    }//GEN-LAST:event_jButton3ActionPerformed

