    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty()
                || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Favor de llenar todos los datos");
        else {
            int id = (int) Cliente[0];
            String nom = jTextField1.getText();
            String app = jTextField3.getText();
            String apm = jTextField4.getText();
            String tel = jTextField2.getText();
            String dir = jTextField6.getText();
            String loc = jTextField7.getText();
            String mail = jTextField5.getText();
            BD.ModificarCliente(id, nom, app, apm, tel, dir, loc, mail);
            this.setVisible(false);
            InterfaceGestionCliente GestionCliente = new InterfaceGestionCliente(BD);
            GestionCliente.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

